export function SocialLinks({
  variant = 'contact',
}: {
  variant?: Variant;
}) {
  const isHero = variant === 'hero';
  const isFooter = variant === 'footer';

  return (
    <div
      className={
        isHero
          ? 'flex flex-row gap-3 sm:gap-3 lg:flex-col lg:items-center'
          : 'flex flex-wrap gap-3'
      }
      role="list"
    >
      {socialLinks.map((s, i) => (
        <motion.a
          key={s.id}
          role="listitem"
          href={s.href}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={s.ariaLabel}
          title={s.label}
          className={`group relative flex shrink-0 items-center justify-center rounded-xl border bg-white/[0.04] transition hover:bg-white/[0.08] hover:shadow-[0_0_20px_rgba(34,211,238,0.25)] hover:border-cyan-400/50 ${
            isHero
              ? 'h-12 w-12 border-white/15 sm:h-11 sm:w-11 lg:h-11 lg:w-11 text-slate-200 hover:text-white'
              : ''
          } ${
            variant === 'contact'
              ? 'h-[52px] min-w-[52px] border-white/12 px-4 text-sm font-medium text-slate-100 hover:border-cyan-400/35'
              : ''
          } ${
            isFooter
              ? 'h-9 w-9 border-white/10 text-slate-400 hover:border-cyan-500/35 hover:text-cyan-300'
              : ''
          }`}
          initial={{ opacity: 0, y: isHero ? 8 : 0 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.9 + i * 0.06, duration: 0.35 }}
          whileHover={{ scale: 1.06 }}
          whileTap={{ scale: 0.96 }}
        >
          {(variant === 'contact' ? (
            <span className="flex items-center gap-3 px-2">
              <svg
                className="shrink-0 text-cyan-400"
                xmlns="http://www.w3.org/2000/svg"
                width={22}
                height={22}
                viewBox="0 0 24 24"
                aria-hidden
              >
                {icons[s.id]}
              </svg>
              <span>{s.label}</span>
            </span>
          ) : (
            <svg
              className={`shrink-0 ${isHero ? 'scale-105' : ''}`}
              xmlns="http://www.w3.org/2000/svg"
              width={isFooter ? 18 : isHero ? 22 : 20}
              height={isFooter ? 18 : isHero ? 22 : 20}
              viewBox="0 0 24 24"
              aria-hidden
            >
              {icons[s.id]}
            </svg>
          ))}
        </motion.a>
      ))}
    </div>
  );
}
